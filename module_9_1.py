my_num = [1, 2, 5, 7, 12, 11, 35, 4, 89, 10]
print('\033[92m'+ "Дан список целых чисел:" + str(my_num) + '\033[0m')
print('\033[91m' + 'ЗАДАЧА! "Необходимо в этом списке оставить нечётные квадраты чисел"' + '\033[0m')
def f1(x):
    return x ** 2
def f2(x):
    return x % 2

print('\033[94m' + 'Используем функции map и filter' + '\033[0m')
result = map (f1,filter(f2,my_num))

print('\033[92m' + "РЕЗУЛЬТАТ " + str(list(result)) + '\033[0m')

